     $       ,I10,2F12.7,       '     DADMPK     K+ K- PI+     ',9X,1H*
     $ /,' *',I10,2F12.7,       '     DADMPK     K0B K0 PI+    ',9X,1H*
     $ /,' *',I10,2F12.7,       '     DADMPK     K+ K0B PI0    ',9X,1H*
